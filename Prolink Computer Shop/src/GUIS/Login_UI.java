    private void BTN_LOGINActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_LOGINActionPerformed
     String username = TXT_USERNAME.getText();
     String password = TXT_PASSWORD.getText();
     
     if(username.isEmpty()|| password.isEmpty()){
         JOptionPane.showMessageDialog(this, "Please fill all fields");
         return;
     }
         boolean loginSuccess = false;
         
      try {
        BufferedReader br = new BufferedReader(
                new FileReader("database/users.txt")
        );
   String line;

        // 2. Read file line by line
        while ((line = br.readLine()) != null) {

            // 3. Split username and password
            String[] data = line.split(",");

            String fileUsername = data[0];
            String filePassword = data[1];

            // 4. Compare
            if (username.equals(fileUsername) && password.equals(filePassword)) {
                loginSuccess = true;
                break;
            }
        }

        br.close();

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error reading user data");
        return;
    }

    // 5. Result
    if (loginSuccess) {
        JOptionPane.showMessageDialog(this, "Login Successful!");
        // open next JFrame here
        Menu_UI mui = new Menu_UI();
        mui.setVisible(true);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "Invalid Username or Password");
    }
        
     
    }//GEN-LAST:event_BTN_LOGINActionPerformed

